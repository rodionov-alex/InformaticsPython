# Четвертый байт имеет 3 свободных бита и две единицы поодряд перед этими свободными битами. Таким образом, чтобы
# в чяетвертом бите было 3 единицы подряд нужно сочетание нулей и единиц в свободных битах с единицей в самом левом
# свободном бите: 100, 101, 110, 111. Итого 4 ip-адреса в сети будут иметь сочетание трех подряд идущих единиц.
