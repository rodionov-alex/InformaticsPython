# Целочисленный (int)
a = 5
print('a =', a, "имеет тип", type(a))
# Вещественный (float)
a = 5.4
print('a =', a, "имеет тип", type(a))
# Булевый (bool)
a = True
print('a =', a, "имеет тип", type(a))
# Строковый (str)
a = 'строка'
print('a = \'', a, "\' имеет тип ", type(a), sep='')
# Список (list)
a = [3, 4, 5]
print('a = ', a, " имеет тип ", type(a), sep='')
# Кортеж (tuple)
a = (3, 4, 5)
print('a = ', a, " имеет тип ", type(a), sep='')
# Множество (set)
a = {3, 4, 5}
print('a = ', a, " имеет тип ", type(a), sep='')
# Словарь (dict)
a = {'k3': 3, 'k4': 4, 'k5': 5}
print('a = ', a, " имеет тип ", type(a), sep='')
print()

