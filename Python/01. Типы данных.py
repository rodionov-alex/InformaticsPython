# Последовательность чисел (range).
# range() хоть и выглядит как функция, на самом деле является типом.
# range(stop) - создание последовательности чисел в интервале [0, stop).
# range(start, stop, step=1) - создание последовательности чисел в интервале [strat, stop) с шагом step который
# по умолчанию 1.
print('range(10) =', list(range(10)))              # [0; 10) с шагом 1
print('range(10, 20) =', list(range(10, 20)))      # [10; 10) с шагом 1
print('range(0, 20, 2) =', list(range(0, 20, 2)))  # [0; 20) с шагом 2
